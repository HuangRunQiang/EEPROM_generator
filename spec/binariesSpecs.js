describe("eeprom.h", function() {
  
  beforeEach(function() {
    jasmine.addMatchers(customMatchers);
  });

  it("should return expected file for given byte array", function() {
    // arrange
    const bytes = [
      0x05,0x0E,0x00,0xCC,0x88,0x13,0xFF,0x00,0x00,0x00,0x40,0xC0,0x00,0x00,0x7F,0x00,
      0x37,0x13,0x00,0x00,0x53,0x92,0x00,0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
      0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x10,0x80,0x00,0x80,0x10,0x80,0x00,
    ];
    // act
    const result = toEsiEepromH(bytes);
    // assert
    const expected = 
`
unsigned char esiEepromData[] = {
0x05,0x0E,0x00,0xCC,0x88,0x13,0xFF,0x00,0x00,0x00,0x40,0xC0,0x00,0x00,0x7F,0x00,
0x37,0x13,0x00,0x00,0x53,0x92,0x00,0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x10,0x80,0x00,0x80,0x10,0x80,0x00};`;
    expect(result).toEqualLines(expected);
  });
});
